"""
Add Strings - Сложение двух строк, представляющих числа

Алгоритм решения:
1. Определяем максимальную длину из двух строк
2. Проходим по разрядам справа налево (от младших к старшим)
3. Для каждого разряда берем соответствующую цифру из каждой строки (0 если разряд отсутствует)
4. Складываем цифры с учетом переноса (carry) с предыдущего разряда
5. Записываем остаток от деления на 10 в результат
6. Обновляем перенос как целую часть от деления на 10
7. Если после обработки всех разрядов остался перенос, добавляем его в результат
8. Возвращаем результат в обратном порядке (так как мы строили его справа налево)
"""

class Solution:
    def addStrings(self, num1: str, num2: str) -> str:
        max_length = max(len(num1), len(num2))
        carry = 0
        result = []
        
        # Проходим по разрядам справа налево
        for i in range(1, max_length + 1):
            # Получаем цифры текущего разряда (0 если разряд отсутствует)
            digit1 = int(num1[-i]) if i <= len(num1) else 0
            digit2 = int(num2[-i]) if i <= len(num2) else 0
            
            # Складываем с учетом переноса
            digit_sum = digit1 + digit2 + carry
            carry = digit_sum // 10
            result.append(str(digit_sum % 10))
        
        # Если остался перенос, добавляем его
        if carry > 0:
            result.append(str(carry))
        
        return ''.join(result[::-1])
