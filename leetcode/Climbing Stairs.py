"""
Climbing Stairs - Количество способов подняться по лестнице

Алгоритм решения:
1. Для n <= 2 возвращаем n (1 способ для 1 ступени, 2 способа для 2 ступеней)
2. Используем динамическое программирование с двумя переменными
3. a и b представляют количество способов для (i-2) и (i-1) ступеней соответственно
4. Для каждой следующей ступени количество способов = a + b
5. Обновляем a = b, b = a + b для следующей итерации
6. Возвращаем b как количество способов для n ступеней
"""

class Solution:
    def climbStairs(self, n: int) -> int:
        if n <= 2:
            return n
        
        # Количество способов для (i-2) и (i-1) ступеней
        prev_prev = 1  # для 1 ступени
        prev = 2       # для 2 ступеней
        
        for i in range(3, n + 1):
            current = prev_prev + prev
            prev_prev = prev
            prev = current
        
        return prev
