"""
Is Subsequence - Проверка, является ли строка подпоследовательностью

Алгоритм решения:
1. Используем два указателя: один для строки s, другой для строки t
2. Проходим по строке t, ища символы из строки s в том же порядке
3. Если находим совпадающий символ, перемещаем указатель s вперед
4. В любом случае перемещаем указатель t вперед
5. Если указатель s достиг конца строки, значит s является подпоследовательностью t
6. Возвращаем True, если все символы s найдены в правильном порядке, иначе False
"""

class Solution:
    def isSubsequence(self, s: str, t: str) -> bool:
        s_index = t_index = 0
        
        while s_index < len(s) and t_index < len(t):
            if s[s_index] == t[t_index]:
                s_index += 1
            t_index += 1
        
        return s_index == len(s)