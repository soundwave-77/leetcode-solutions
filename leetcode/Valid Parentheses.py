"""
Valid Parentheses - Проверка корректности скобок

Алгоритм решения:
1. Создаем словарь соответствия открывающих и закрывающих скобок
2. Используем стек для отслеживания открывающих скобок
3. Проходим по каждому символу в строке
4. Если символ - открывающая скобка, добавляем в стек
5. Если символ - закрывающая скобка:
   - Проверяем, что стек не пуст
   - Проверяем, что закрывающая скобка соответствует последней открывающей
   - Если не соответствует, возвращаем False
   - Иначе удаляем последнюю открывающую скобку из стека
6. В конце проверяем, что стек пуст (все скобки закрыты)
"""

class Solution:
    def isValid(self, s: str) -> bool:
        bracket_pairs = {"(": ")", "{": "}", "[": "]"}
        stack = []
        
        for char in s:
            if char in bracket_pairs:
                stack.append(char)
            else:
                if not stack or bracket_pairs[stack[-1]] != char:
                    return False
                stack.pop()
        
        return len(stack) == 0